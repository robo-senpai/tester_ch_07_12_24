# dane od uzytkownika
number1 = float(input("Podaj pierwsza liczbe: "))
number2 = float(input("Podaj druga liczbe: "))

# operacje matematyczne
add = number1 + number2
potegowanie = number1 ** number2

print(f"Suma twoich liczb: {number1} oraz {number2} to {add}")
print(f"Natomiast {number1} do potegi {number2} to {potegowanie}")
